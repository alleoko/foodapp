package com.example.finaljjkitchen.Model;

public class Catering {

    private int ID;
    private String Catering_ProductId;
    private String Catering_CustomerName;
    private String Catering_ProductName;
    private String Catering_Price;
    private String Catering_Quantity;
    private String Catering_Address;
    private String Discount;
    private String Date;
    private String Catering_Phone;

    public String getCatering_Phone() {
        return Catering_Phone;
    }

    public void setCatering_Phone(String catering_Phone) {
        Catering_Phone = catering_Phone;
    }




    public String getDate() {
        return Date;
    }

    public void setDate(String date) {
        Date = date;
    }




    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getCatering_ProductId() {
        return Catering_ProductId;
    }

    public void setCatering_ProductId(String catering_ProductId) {
        Catering_ProductId = catering_ProductId;
    }

    public String getCatering_CustomerName() {
        return Catering_CustomerName;
    }

    public void setCatering_CustomerName(String catering_CustomerName) {
        Catering_CustomerName = catering_CustomerName;
    }

    public String getCatering_ProductName() {
        return Catering_ProductName;
    }

    public void setCatering_ProductName(String catering_ProductName) {
        Catering_ProductName = catering_ProductName;
    }

    public String getCatering_Price() {
        return Catering_Price;
    }

    public void setCatering_Price(String catering_Price) {
        Catering_Price = catering_Price;
    }

    public String getCatering_Quantity() {
        return Catering_Quantity;
    }

    public void setCatering_Quantity(String catering_Quantity) {
        Catering_Quantity = catering_Quantity;
    }

    public String getCatering_Address() {
        return Catering_Address;
    }

    public void setCatering_Address(String catering_Address) {
        Catering_Address = catering_Address;
    }

    public String getDiscount() {
        return Discount;
    }

    public void setDiscount(String discount) {
        Discount = discount;
    }
}


